fn paragraph_format(document: &Document, style: ParagraphStyle) -> EffectiveParagraphFormat {
  let theme = &document.theme;
  let normal = EffectiveParagraphFormat {
    font_size: theme.body_font_size,
    font_family: theme.default_font_family.clone(),
    bold: false,
    italic: false,
    color: theme.default_text_color,
    align: ParagraphAlign::Left,
    spacing_before: px(0.0),
    spacing_after: theme.paragraph_after,
    line_spacing: theme.line_spacing,
    border: None,
    underline: UnderlineKind::None,
  };

  match style {
    ParagraphStyle::Normal => normal,
    ParagraphStyle::Pocket => EffectiveParagraphFormat {
      font_size: theme.pocket_font_size,
      bold: true,
      align: ParagraphAlign::Center,
      spacing_before: theme.pocket_before,
      spacing_after: px(0.0),
      border: Some(ParagraphBorder {
        width: theme.pocket_border_width,
        space_x: theme.pocket_border_space_x,
        space_y: theme.pocket_border_space_y,
      }),
      ..normal
    },
    ParagraphStyle::Hat => EffectiveParagraphFormat {
      font_size: theme.hat_font_size,
      bold: true,
      align: ParagraphAlign::Center,
      spacing_before: theme.hat_before,
      spacing_after: px(0.0),
      underline: UnderlineKind::Double,
      ..normal
    },
    ParagraphStyle::Block => EffectiveParagraphFormat {
      font_size: theme.block_font_size,
      bold: true,
      align: ParagraphAlign::Center,
      spacing_before: theme.block_before,
      spacing_after: px(0.0),
      underline: UnderlineKind::Single,
      ..normal
    },
    ParagraphStyle::Tag => EffectiveParagraphFormat {
      font_size: theme.tag_font_size,
      bold: true,
      spacing_before: theme.tag_before,
      spacing_after: px(0.0),
      ..normal
    },
    ParagraphStyle::Analytic => EffectiveParagraphFormat {
      font_size: theme.tag_font_size,
      bold: true,
      color: theme.analytic_color,
      spacing_before: theme.tag_before,
      spacing_after: px(0.0),
      ..normal
    },
    ParagraphStyle::Undertag => EffectiveParagraphFormat {
      font_size: theme.undertag_font_size,
      font_family: theme.default_font_family.clone(),
      italic: true,
      color: theme.undertag_color,
      spacing_after: px(0.0),
      ..normal
    },
  }
}

fn run_format(document: &Document, paragraph: EffectiveParagraphFormat, styles: RunStyles) -> EffectiveRunFormat {
  let theme = &document.theme;
  let mut format = EffectiveRunFormat {
    font_size: paragraph.font_size,
    font_family: paragraph.font_family,
    bold: paragraph.bold,
    italic: paragraph.italic,
    color: paragraph.color,
    underline: paragraph.underline,
    highlight: styles.highlight.map(|highlight| match highlight {
      HighlightStyle::Spoken => theme.highlight_spoken,
      HighlightStyle::Insert => theme.highlight_insert,
      HighlightStyle::Alternative => theme.highlight_alternative,
    }),
    border_width: px(0.0),
  };

  if styles.style_underline {
    format.font_size = theme.body_font_size;
    format.bold = false;
    format.underline = UnderlineKind::Single;
  }
  if styles.direct_underline {
    format.underline = UnderlineKind::Single;
  }
  if styles.cite {
    format.font_size = theme.cite_font_size;
    format.bold = true;
    format.underline = UnderlineKind::None;
  }
  if styles.emphasis {
    format.font_family = theme.default_font_family.clone();
    format.font_size = theme.cite_font_size;
    format.bold = true;
    format.italic = false;
    format.underline = UnderlineKind::Single;
    format.border_width = theme.emphasis_border_width;
  }

  format
}

fn build_layout(document: &Document, width: Pixels, previous_layout: Option<&LayoutState>, window: &mut Window, cx: &mut App) -> LayoutState {
  let pageless_left = document.theme.pageless_inset_x;
  let pageless_width = (width - document.theme.pageless_inset_x * 2.0).max(px(1.0));
  let mut y = document.theme.pageless_inset_top;
  let mut paragraphs = Vec::with_capacity(document.paragraphs.len());
  let mut max_width = width;
  let previous_layout = previous_layout.filter(|layout| layout.width == width);

  for (paragraph_ix, paragraph) in document.paragraphs.iter().enumerate() {
    let p_format = paragraph_format(document, paragraph.style);
    y += p_format.spacing_before;
    let cache_key = paragraph_cache_key(document, paragraph);

    if let Some(cached) = previous_layout
      .and_then(|layout| layout.paragraphs.get(paragraph_ix))
      .filter(|cached| cached.cache_key == cache_key)
    {
      let mut laid_out_paragraph = cached.clone();
      laid_out_paragraph.shift_y(y);
      for line in &laid_out_paragraph.lines {
        max_width = max_width.max(line.origin.x + line.width);
      }
      y = laid_out_paragraph.bottom + p_format.spacing_after;
      paragraphs.push(laid_out_paragraph);
      continue;
    }

    let border = p_format.border;
    let border_inset = border.map_or(px(0.0), |border| border.width + border.space_x);
    let content_left = pageless_left + border_inset;
    let content_top = border.map_or(px(0.0), |border| border.width + border.space_y);
    let content_width = (pageless_width - border_inset * 2.0).max(px(1.0));
    let paragraph_text = paragraph_text(document, paragraph);
    let lines = wrap_lines(document, paragraph, p_format.clone(), &paragraph_text, content_width, window, cx);

    let mut laid_out_lines = Vec::with_capacity(lines.len());
    let mut line_y = y + content_top;
    for mut line in lines {
      line.origin.x = content_left
        + match p_format.align {
          ParagraphAlign::Left => px(0.0),
          ParagraphAlign::Center => (content_width - line.width).max(px(0.0)) / 2.0,
        };
      line.origin.y = line_y;
      line_y += line.line_height;
      max_width = max_width.max(line.origin.x + line.width);
      laid_out_lines.push(line);
    }

    let bottom = line_y + content_top;
    let mut borders = Vec::new();
    if let Some(border) = border {
      push_box_rules(
        &mut borders,
        Bounds::new(point(pageless_left, y), size(pageless_width, bottom - y)),
        border.width,
        document.theme.default_text_color,
      );
    }

    paragraphs.push(LaidOutParagraph {
      index: paragraph_ix,
      cache_key,
      len: paragraph_text.len(),
      top: y,
      bottom,
      lines: laid_out_lines,
      borders,
    });
    y = bottom + p_format.spacing_after;
  }

  LayoutState {
    paragraphs,
    bounds: None,
    size: size(max_width, y + document.theme.pageless_inset_bottom),
    width,
    snap_underline_rules_to_pixels: document.theme.snap_underline_rules_to_pixels,
  }
}

fn wrap_lines(
  document: &Document,
  paragraph: &Paragraph,
  p_format: EffectiveParagraphFormat,
  text: &str,
  max_width: Pixels,
  window: &mut Window,
  cx: &mut App,
) -> Vec<LaidOutLine> {
  if text.is_empty() {
    return vec![shape_line(document, paragraph, p_format, text, 0..0, window, cx)];
  }

  let mut lines = Vec::new();
  let mut start = 0;
  let break_ends = wrap_break_ends(text);
  let mut break_cursor = 0;

  while start < text.len() {
    while break_cursor < break_ends.len() && break_ends[break_cursor] <= start {
      break_cursor += 1;
    }
    let mut last_break = None;
    let mut wrapped = false;
    let mut scan_ix = break_cursor;

    while scan_ix < break_ends.len() {
      let break_at = break_ends[scan_ix];
      let candidate_width = measure_line_width(document, paragraph, &p_format, text, start..break_at, break_at - start, window);
      if candidate_width > max_width {
        let line_end = last_break
          .filter(|break_at| *break_at > start)
          .unwrap_or_else(|| first_overflow_line_end(document, paragraph, &p_format, text, start, break_at, max_width, window));
        lines.push(shape_line(
          document,
          paragraph,
          p_format.clone(),
          text[start..line_end].trim_end(),
          start..line_end,
          window,
          cx,
        ));
        start = skip_leading_whitespace(text, line_end);
        wrapped = true;
        break;
      }
      last_break = Some(break_at);
      scan_ix += 1;
    }

    if wrapped {
      continue;
    }

    let remaining_width = measure_line_width(document, paragraph, &p_format, text, start..text.len(), text.len() - start, window);
    if remaining_width <= max_width {
      lines.push(shape_line(document, paragraph, p_format, &text[start..], start..text.len(), window, cx));
      break;
    }

    let line_end = last_break
      .filter(|break_at| *break_at > start)
      .unwrap_or_else(|| first_overflow_line_end(document, paragraph, &p_format, text, start, text.len(), max_width, window));
    lines.push(shape_line(
      document,
      paragraph,
      p_format.clone(),
      text[start..line_end].trim_end(),
      start..line_end,
      window,
      cx,
    ));
    start = skip_leading_whitespace(text, line_end);
  }

  lines
}

fn wrap_break_ends(text: &str) -> Vec<usize> {
  text
    .char_indices()
    .filter_map(|(byte_ix, ch)| is_wrap_break(ch).then_some(byte_ix + ch.len_utf8()))
    .collect()
}

fn is_wrap_break(ch: char) -> bool {
  ch.is_whitespace() || matches!(ch, '-' | '/' | ',' | ';' | ':')
}

fn skip_leading_whitespace(text: &str, mut byte: usize) -> usize {
  while byte < text.len() && text[byte..].chars().next().is_some_and(char::is_whitespace) {
    byte += text[byte..].chars().next().unwrap().len_utf8();
  }
  byte
}

fn first_overflow_line_end(
  document: &Document,
  paragraph: &Paragraph,
  p_format: &EffectiveParagraphFormat,
  text: &str,
  start: usize,
  limit: usize,
  max_width: Pixels,
  window: &mut Window,
) -> usize {
  let chars: Vec<_> = text[start..limit]
    .char_indices()
    .map(|(relative_byte, ch)| {
      let byte_ix = start + relative_byte;
      (byte_ix, byte_ix + ch.len_utf8(), ch)
    })
    .collect();
  if chars.is_empty() {
    return limit;
  }

  let mut low = 0;
  let mut high = chars.len();
  while low < high {
    let mid = (low + high) / 2;
    let end = chars[mid].1;
    let width = measure_line_width(document, paragraph, p_format, text, start..end, end - start, window);
    if width > max_width {
      high = mid;
    } else {
      low = mid + 1;
    }
  }

  let Some((byte_ix, end, ch)) = chars.get(low).copied() else {
    return limit;
  };
  if is_wrap_break(ch) || byte_ix == start { end } else { byte_ix }
}

fn measure_line_width(
  document: &Document,
  paragraph: &Paragraph,
  p_format: &EffectiveParagraphFormat,
  paragraph_text: &str,
  source_range: Range<usize>,
  rendered_len: usize,
  window: &mut Window,
) -> Pixels {
  let mut width = px(0.0);
  let rendered_text = &paragraph_text[source_range.start..source_range.start + rendered_len];
  for fragment in fragments_for_range(paragraph, &source_range, rendered_len) {
    let text = &rendered_text[fragment.line_range.clone()];
    if text.is_empty() {
      continue;
    }
    let format = run_format(document, p_format.clone(), fragment.styles);
    let shaped = shape_fragment(window, text, format.clone());
    if format.border_width > px(0.0) {
      width += document.theme.box_padding_left;
    }
    width += shaped.width;
    if format.border_width > px(0.0) {
      width += document.theme.box_padding_right;
    }
  }
  width
}

fn shape_line(
  document: &Document,
  paragraph: &Paragraph,
  p_format: EffectiveParagraphFormat,
  line_text: &str,
  source_range: Range<usize>,
  window: &mut Window,
  cx: &mut App,
) -> LaidOutLine {
  let fragments = fragments_for_range(paragraph, &source_range, line_text.len());
  let mut x = px(0.0);
  let mut segments = Vec::with_capacity(fragments.len().max(1));
  let mut ascent = px(0.0);
  let mut descent = px(0.0);

  for fragment in fragments {
    let text = &line_text[fragment.line_range.clone()];
    if text.is_empty() {
      continue;
    }
    let format = run_format(document, p_format.clone(), fragment.styles);
    let shaped = shape_fragment(window, text, format.clone());
    let width = shaped.width;
    let box_margin_left = if format.border_width > px(0.0) {
      document.theme.box_padding_left
    } else {
      px(0.0)
    };
    let box_margin_right = if format.border_width > px(0.0) {
      document.theme.box_padding_right
    } else {
      px(0.0)
    };
    let segment_ascent = shaped.ascent;
    let segment_descent = shaped.descent;
    ascent = ascent.max(segment_ascent);
    descent = descent.max(segment_descent);
    x += box_margin_left;
    segments.push(LaidOutSegment {
      shaped,
      x,
      width,
      format: format.clone(),
      ascent: segment_ascent,
      descent: segment_descent,
      font_size: format.font_size,
      start_byte: fragment.source_start,
    });
    x += width + box_margin_right;
  }

  if segments.is_empty() {
    let format = run_format(document, p_format.clone(), RunStyles::default());
    let shaped = shape_fragment(window, "", format.clone());
    ascent = shaped.ascent;
    descent = shaped.descent;
    segments.push(LaidOutSegment {
      shaped,
      format: format.clone(),
      x: px(0.0),
      width: px(0.0),
      ascent,
      descent,
      font_size: format.font_size,
      start_byte: source_range.start,
    });
  } else {
    ascent = segments
      .iter()
      .map(|segment| segment.ascent)
      .fold(px(0.0), Pixels::max);
    descent = segments
      .iter()
      .map(|segment| segment.descent)
      .fold(px(0.0), Pixels::max);
  }

  let max_font_size = segments
    .iter()
    .map(|segment| segment.font_size)
    .fold(p_format.font_size, Pixels::max);
  let line_gap = max_font_size * document.theme.line_gap_fraction;
  let line_height = (ascent + descent + line_gap) * p_format.line_spacing;
  let mut line = LaidOutLine {
    origin: point(px(0.0), px(0.0)),
    line_height,
    ascent,
    descent,
    width: x,
    start_byte: source_range.start,
    end_byte: source_range.end,
    segments,
    rects: Vec::new(),
    underlines: Vec::new(),
  };
  line.rects = rects_for_line(document, &line);
  line.underlines = underlines_for_line(document, &line, cx);
  line
}

fn shape_fragment(window: &mut Window, text: &str, format: EffectiveRunFormat) -> ShapedLine {
  let mut run_font = font(format.font_family);
  run_font.weight = if format.bold { FontWeight::BOLD } else { FontWeight::NORMAL };
  run_font.style = if format.italic { FontStyle::Italic } else { FontStyle::Normal };
  let run = GpuiTextRun {
    len: text.len(),
    font: run_font,
    color: format.color,
    background_color: None,
    underline: None,
    strikethrough: None,
  };
  window
    .text_system()
    .shape_line(SharedString::new(text), format.font_size, &[run], None)
}

#[derive(Clone)]
struct VisualFragment {
  styles: RunStyles,
  line_range: Range<usize>,
  source_start: usize,
}

fn fragments_for_range(paragraph: &Paragraph, range: &Range<usize>, rendered_len: usize) -> Vec<VisualFragment> {
  let mut byte_offset = 0;
  let mut line_offset = 0;
  let mut remaining = rendered_len;
  let mut fragments = Vec::with_capacity(paragraph.runs.len());
  for run in &paragraph.runs {
    let run_start = byte_offset;
    let run_end = byte_offset + run.len;
    byte_offset = run_end;
    let start = run_start.max(range.start);
    let end = run_end.min(range.end);
    if start >= end || remaining == 0 {
      continue;
    }
    let len = (end - start).min(remaining);
    fragments.push(VisualFragment {
      styles: run.styles,
      line_range: line_offset..line_offset + len,
      source_start: start,
    });
    remaining -= len;
    line_offset += len;
  }
  fragments
}

fn rects_for_line(document: &Document, line: &LaidOutLine) -> Vec<RunRect> {
  let mut backgrounds = Vec::new();
  let mut borders = Vec::new();
  let text_top = line.baseline_y() - line.ascent;
  let text_bottom = line.baseline_y() + line.descent;
  let max_font_size = line
    .segments
    .iter()
    .map(|segment| segment.font_size)
    .fold(px(0.0), Pixels::max);
  let bottom_pad = max_font_size * document.theme.highlight_bottom_extra_fraction;
  // Highlights share the same theoretical top as Word's inline run border:
  // even when no border is painted, the highlight should look like it fills
  // the box that would be drawn for the run.
  let paint_top = text_top - document.theme.box_padding_top;
  let paint_height = (text_bottom + bottom_pad - paint_top).max(px(1.0));

  for segment in &line.segments {
    let highlight_pad_left = if segment.format.border_width > px(0.0) {
      document.theme.box_padding_left
    } else {
      document.theme.highlight_pad_x
    };
    let highlight_pad_right = if segment.format.border_width > px(0.0) {
      document.theme.box_padding_right
    } else {
      document.theme.highlight_pad_x
    };
    let paint_box = Bounds::new(
      point(segment.x - highlight_pad_left, paint_top),
      size((segment.width + highlight_pad_left + highlight_pad_right).max(px(1.0)), paint_height),
    );

    if let Some(background) = segment.format.highlight {
      backgrounds.push(RunRect {
        bounds: paint_box,
        color: background,
        snap: RuleSnap::None,
      });
    }
    if segment.format.border_width > px(0.0) {
      let box_bounds = Bounds::new(
        point(segment.x - document.theme.box_padding_left, text_top - document.theme.box_padding_top),
        size(
          (segment.width + document.theme.box_padding_left + document.theme.box_padding_right).max(px(1.0)),
          (text_bottom - text_top + document.theme.box_padding_top + document.theme.box_padding_bottom).max(px(1.0)),
        ),
      );
      push_box_rules(
        &mut borders,
        box_bounds,
        document.theme.emphasis_border_paint_width,
        document.theme.default_text_color,
      );
    }
  }
  // Word paints fills before border rules. Keeping all run borders after all
  // run highlights prevents a following highlighted run from hiding the right
  // edge of the previous boxed run.
  backgrounds.extend(borders);
  backgrounds
}

fn push_box_rules(rects: &mut Vec<RunRect>, bounds: Bounds<Pixels>, thickness: Pixels, color: Hsla) {
  rects.push(RunRect {
    bounds: Bounds::new(bounds.origin, size(bounds.size.width, thickness)),
    color,
    snap: RuleSnap::Horizontal,
  });
  rects.push(RunRect {
    bounds: Bounds::new(
      point(bounds.origin.x, bounds.origin.y + bounds.size.height - thickness),
      size(bounds.size.width, thickness),
    ),
    color,
    snap: RuleSnap::Horizontal,
  });
  rects.push(RunRect {
    bounds: Bounds::new(bounds.origin, size(thickness, bounds.size.height)),
    color,
    snap: RuleSnap::Vertical,
  });
  rects.push(RunRect {
    bounds: Bounds::new(
      point(bounds.origin.x + bounds.size.width - thickness, bounds.origin.y),
      size(thickness, bounds.size.height),
    ),
    color,
    snap: RuleSnap::Vertical,
  });
}

fn underlines_for_line(document: &Document, line: &LaidOutLine, cx: &mut App) -> Vec<Decoration> {
  let mut underlines = Vec::new();
  let baseline = line.baseline_y();
  for segment in &line.segments {
    match segment.format.underline {
      UnderlineKind::None => {},
      UnderlineKind::Single => {
        let (offset, thickness) = single_underline_metrics_for_segment(segment, document, cx);
        underlines.push(Decoration {
          bounds: Bounds::new(point(segment.x, baseline + offset), size(segment.width.max(px(1.0)), thickness)),
          color: document.theme.default_text_color,
        });
      },
      UnderlineKind::Double => {
        let (offset, thickness) = double_underline_metrics_for_segment(document);
        let y = baseline + offset;
        underlines.push(Decoration {
          bounds: Bounds::new(point(segment.x, y), size(segment.width.max(px(1.0)), thickness)),
          color: document.theme.default_text_color,
        });
        underlines.push(Decoration {
          bounds: Bounds::new(
            point(segment.x, y + thickness + document.theme.double_underline_gap),
            size(segment.width.max(px(1.0)), thickness),
          ),
          color: document.theme.default_text_color,
        });
      },
    }
  }
  underlines
}

fn single_underline_metrics_for_segment(segment: &LaidOutSegment, document: &Document, cx: &mut App) -> (Pixels, Pixels) {
  // GPUI exposes glyph bounds in font coordinates. For Calibri, the
  // underscore bbox is below the baseline. The origin is the lower
  // edge of the glyph box on this metric path; Word positions an
  // underline at the top of the underscore glyph, so subtract the
  // glyph height from the baseline-to-origin distance.
  //
  // On Linux, GPUI's `typographic_bounds` is a stub returning
  // `origin = (0, 0)` with the advance box as the size (see gpui's
  // platform/linux/text_system.rs). That makes the formula collapse to 0
  // and paint the underline at the baseline, cutting through descenders.
  // So on Linux we skip the glyph-derived path entirely and use the
  // theme's Word-derived fallback constant.
  #[cfg(target_os = "linux")]
  let offset = {
    let _ = (segment, cx); // silence unused warnings on linux
    document.theme.underline_fallback_top_from_baseline
  };
  #[cfg(not(target_os = "linux"))]
  let offset = regular_underscore_bounds(segment, cx)
    .map(|bounds| (bounds.origin.y.abs() - bounds.size.height).max(px(0.0)))
    .unwrap_or(document.theme.underline_fallback_top_from_baseline);
  (offset, document.theme.underline_rule_thickness)
}

fn double_underline_metrics_for_segment(document: &Document) -> (Pixels, Pixels) {
  (document.theme.double_underline_top_from_baseline, document.theme.underline_rule_thickness)
}

#[cfg(not(target_os = "linux"))]
fn regular_underscore_bounds(segment: &LaidOutSegment, cx: &mut App) -> Option<Bounds<Pixels>> {
  let mut underline_font = font(segment.format.font_family.clone());
  // Word's underline metric follows the regular face's underscore metrics;
  // bold text remains bold, but the underline itself does not get bolded.
  underline_font.weight = FontWeight::NORMAL;
  underline_font.style = if segment.format.italic { FontStyle::Italic } else { FontStyle::Normal };
  let font_id = cx.text_system().resolve_font(&underline_font);
  cx.text_system()
    .typographic_bounds(font_id, segment.font_size, '_')
    .ok()
}

fn paint_layout(layout: &LayoutState, selection: Option<&EditorSelection>, window: &mut Window, cx: &mut App) {
  let Some(bounds) = layout.bounds else {
    return;
  };
  let content_mask = window.content_mask().bounds;
  for paragraph in &layout.paragraphs {
    if !paragraph_intersects_mask(paragraph, bounds.origin, content_mask) {
      continue;
    }
    for border in &paragraph.borders {
      let border_bounds = snap_rule_bounds(border.bounds.shift(bounds.origin), border.snap, window);
      window.paint_quad(fill(border_bounds, Background::from(border.color)));
    }
  }
  for paragraph in &layout.paragraphs {
    if !paragraph_intersects_mask(paragraph, bounds.origin, content_mask) {
      continue;
    }
    for line in &paragraph.lines {
      if !line_intersects_mask(line, bounds.origin, content_mask) {
        continue;
      }
      for rect in &line.rects {
        let rect_bounds = snap_rule_bounds(rect.bounds.shift(bounds.origin + line.origin), rect.snap, window);
        window.paint_quad(fill(rect_bounds, Background::from(rect.color)));
      }
    }
  }
  for paragraph in &layout.paragraphs {
    if !paragraph_intersects_mask(paragraph, bounds.origin, content_mask) {
      continue;
    }
    for line in &paragraph.lines {
      if line_intersects_mask(line, bounds.origin, content_mask) {
        paint_line_text(line, bounds.origin + line.origin, content_mask, window, cx);
      }
    }
  }
  for paragraph in &layout.paragraphs {
    if !paragraph_intersects_mask(paragraph, bounds.origin, content_mask) {
      continue;
    }
    for line in &paragraph.lines {
      if !line_intersects_mask(line, bounds.origin, content_mask) {
        continue;
      }
      for underline in &line.underlines {
        let mut underline_bounds = underline.bounds.shift(bounds.origin + line.origin);
        if layout.snap_underline_rules_to_pixels {
          underline_bounds = snap_horizontal_rule_to_device_pixels(underline_bounds, window);
        }
        window.paint_quad(fill(underline_bounds, Background::from(underline.color)));
      }
    }
  }
  if let Some(selection) = selection {
    paint_selection(layout, selection, bounds.origin, content_mask, window);
  }
  if let Some(selection) = selection
    && selection.is_caret()
    && let Some(caret) = caret_bounds(layout, selection.head, bounds.origin)
    && caret.intersects(&content_mask)
  {
    window.paint_quad(fill(caret, black()));
  }
}

fn scroll_rect_into_view(scroll_handle: &ScrollHandle, rect: Bounds<Pixels>, margin: Pixels) {
  let viewport = scroll_handle.bounds();
  if viewport.size.height <= px(0.0) {
    return;
  }

  let top = rect.top() - margin;
  let bottom = rect.bottom() + margin;
  let mut offset = scroll_handle.offset();
  if top < viewport.top() {
    offset.y += viewport.top() - top;
  } else if bottom > viewport.bottom() {
    offset.y -= bottom - viewport.bottom();
  } else {
    return;
  }
  scroll_handle.set_offset(clamp_scroll_offset(scroll_handle, offset));
}

fn scroll_by(scroll_handle: &ScrollHandle, delta_y: Pixels) -> bool {
  if delta_y == px(0.0) {
    return false;
  }
  let old_offset = scroll_handle.offset();
  let mut offset = old_offset;
  offset.y -= delta_y;
  let offset = clamp_scroll_offset(scroll_handle, offset);
  if offset == old_offset {
    return false;
  }
  scroll_handle.set_offset(offset);
  true
}

fn clamp_scroll_offset(scroll_handle: &ScrollHandle, mut offset: Point<Pixels>) -> Point<Pixels> {
  let max = scroll_handle.max_offset();
  offset.x = offset.x.min(px(0.0)).max(-max.width);
  offset.y = offset.y.min(px(0.0)).max(-max.height);
  offset
}

fn drag_autoscroll_step(viewport: Bounds<Pixels>, position: Point<Pixels>) -> Pixels {
  if viewport.size.height <= px(0.0) {
    return px(0.0);
  }

  let edge = px(36.0);
  let max_step = px(28.0);
  if position.y < viewport.top() {
    -(edge + viewport.top() - position.y).min(max_step)
  } else if position.y < viewport.top() + edge {
    -(viewport.top() + edge - position.y).min(max_step)
  } else if position.y > viewport.bottom() {
    (edge + position.y - viewport.bottom()).min(max_step)
  } else if position.y > viewport.bottom() - edge {
    (position.y - (viewport.bottom() - edge)).min(max_step)
  } else {
    px(0.0)
  }
}

fn paragraph_intersects_mask(paragraph: &LaidOutParagraph, origin: Point<Pixels>, mask: Bounds<Pixels>) -> bool {
  vertical_range_intersects(origin.y + paragraph.top, origin.y + paragraph.bottom, mask)
}

fn line_intersects_mask(line: &LaidOutLine, origin: Point<Pixels>, mask: Bounds<Pixels>) -> bool {
  vertical_range_intersects(origin.y + line.origin.y, origin.y + line.origin.y + line.line_height, mask)
}

fn vertical_range_intersects(top: Pixels, bottom: Pixels, mask: Bounds<Pixels>) -> bool {
  let mask_top = mask.origin.y;
  let mask_bottom = mask.origin.y + mask.size.height;
  bottom >= mask_top && top <= mask_bottom
}

fn snap_horizontal_rule_to_device_pixels(mut bounds: Bounds<Pixels>, window: &Window) -> Bounds<Pixels> {
  let scale = window.scale_factor();
  bounds.origin.y = snap_pixel_to_device_grid(bounds.origin.y, scale);
  bounds.size.height = snap_rule_thickness_to_device_grid(bounds.size.height, scale);
  bounds
}

fn snap_rule_bounds(bounds: Bounds<Pixels>, snap: RuleSnap, window: &Window) -> Bounds<Pixels> {
  match snap {
    RuleSnap::None => bounds,
    RuleSnap::Horizontal => snap_horizontal_rule_to_device_pixels(bounds, window),
    RuleSnap::Vertical => snap_vertical_rule_to_device_pixels(bounds, window),
  }
}

fn snap_vertical_rule_to_device_pixels(mut bounds: Bounds<Pixels>, window: &Window) -> Bounds<Pixels> {
  let scale = window.scale_factor();
  bounds.origin.x = snap_pixel_to_device_grid(bounds.origin.x, scale);
  bounds.size.width = snap_rule_thickness_to_device_grid(bounds.size.width, scale);
  bounds
}

fn snap_pixel_to_device_grid(value: Pixels, scale: f32) -> Pixels {
  let value: f32 = value.into();
  px((value * scale).round() / scale)
}

fn snap_rule_thickness_to_device_grid(value: Pixels, scale: f32) -> Pixels {
  let value: f32 = value.into();
  px(((value * scale).round().max(1.0)) / scale)
}

fn paint_selection(layout: &LayoutState, selection: &EditorSelection, origin: Point<Pixels>, content_mask: Bounds<Pixels>, window: &mut Window) {
  if selection.is_caret() {
    return;
  }
  let range = selection.normalized();
  for paragraph in &layout.paragraphs {
    if paragraph.index < range.start.paragraph || paragraph.index > range.end.paragraph {
      continue;
    }
    if !paragraph_intersects_mask(paragraph, origin, content_mask) {
      continue;
    }
    let start = if paragraph.index == range.start.paragraph { range.start.byte } else { 0 };
    let end = if paragraph.index == range.end.paragraph {
      range.end.byte
    } else {
      paragraph.len
    };
    for line in &paragraph.lines {
      if !line_intersects_mask(line, origin, content_mask) {
        continue;
      }
      let line_start = start.max(line.start_byte);
      let line_end = end.min(line.end_byte);
      if line_start >= line_end {
        continue;
      }
      let x1 = x_for_byte(line, line_start);
      let x2 = x_for_byte(line, line_end);
      window.paint_quad(fill(
        Bounds::new(origin + line.origin + point(x1, px(0.0)), size((x2 - x1).max(px(1.0)), line.line_height)),
        hsla(0.0, 0.0, 0.0, 0.22),
      ));
    }
  }
}

fn caret_bounds(layout: &LayoutState, offset: DocumentOffset, origin: Point<Pixels>) -> Option<Bounds<Pixels>> {
  let paragraph = paragraph_layout(layout, offset.paragraph)?;
  let line = paragraph
    .lines
    .iter()
    .find(|line| offset.byte >= line.start_byte && offset.byte <= line.end_byte)
    .or_else(|| paragraph.lines.last())?;
  let x = x_for_byte(line, offset.byte);
  Some(Bounds::new(origin + line.origin + point(x, px(0.0)), size(px(1.0), line.line_height)))
}

fn x_for_byte(line: &LaidOutLine, byte: usize) -> Pixels {
  for segment in &line.segments {
    let segment_end = segment.start_byte + segment.shaped.len();
    if byte <= segment_end {
      return segment.x
        + segment
          .shaped
          .x_for_index(byte.saturating_sub(segment.start_byte));
    }
  }
  line.width
}

fn paint_line_text(line: &LaidOutLine, origin: Point<Pixels>, content_mask: Bounds<Pixels>, window: &mut Window, cx: &mut App) {
  let text_system = cx.text_system().clone();
  let baseline = line.baseline_y();
  for segment in &line.segments {
    let segment_origin = origin + point(segment.x, baseline);
    for run in &segment.shaped.runs {
      let glyph_bounds = text_system
        .bounding_box(run.font_id, segment.font_size)
        .size;
      for glyph in &run.glyphs {
        let glyph_origin = segment_origin + point(glyph.position.x, px(0.0));
        if !Bounds::new(glyph_origin, glyph_bounds).intersects(&content_mask) {
          continue;
        }
        let result = if glyph.is_emoji {
          window.paint_emoji(glyph_origin, run.font_id, glyph.id, segment.font_size)
        } else {
          window.paint_glyph(glyph_origin, run.font_id, glyph.id, segment.font_size, segment.format.color)
        };
        if let Err(error) = result {
          eprintln!("failed to paint glyph: {error}");
        }
      }
    }
  }
}

fn apply_style_to_paragraph_range(document: &mut Document, paragraph_ix: usize, range: Range<usize>, style: RunStyle) {
  if range.start >= range.end {
    return;
  }
  let Some(paragraph) = document.paragraphs.get_mut(paragraph_ix) else {
    return;
  };
  let mut output = Vec::with_capacity(paragraph.runs.len() + 2);
  let mut offset = 0;
  for run in paragraph.runs.drain(..) {
    let run_start = offset;
    let run_end = offset + run.len;
    offset = run_end;
    if run_end <= range.start || run_start >= range.end {
      output.push(run);
      continue;
    }
    let local_start = range.start.saturating_sub(run_start).min(run.len);
    let local_end = (range.end.saturating_sub(run_start)).min(run.len);
    if local_start > 0 {
      output.push(TextRun {
        len: local_start,
        styles: run.styles,
      });
    }
    let mut styles = run.styles;
    styles.apply(style);
    output.push(TextRun {
      len: local_end - local_start,
      styles,
    });
    if local_end < run.len {
      output.push(TextRun {
        len: run.len - local_end,
        styles: run.styles,
      });
    }
  }
  paragraph.runs = merge_adjacent_runs(output);
}

fn merge_adjacent_runs(runs: Vec<TextRun>) -> Vec<TextRun> {
  let mut merged: Vec<TextRun> = Vec::with_capacity(runs.len());
  for run in runs {
    if run.len == 0 {
      continue;
    }
    if let Some(last) = merged.last_mut()
      && last.styles == run.styles
    {
      last.len += run.len;
      continue;
    }
    merged.push(run);
  }
  merged
}

fn paragraph_text(document: &Document, paragraph: &Paragraph) -> String {
  document_text_slice(document, paragraph.byte_range.clone())
}

fn paragraph_text_len(paragraph: &Paragraph) -> usize {
  paragraph.byte_range.end - paragraph.byte_range.start
}

fn pt(value: f32) -> Pixels {
  px(value * 96.0 / 72.0)
}

fn border_eighth_points(value: f32) -> Pixels {
  pt(value / 8.0)
}

trait ShiftBounds {
  fn shift(self, by: Point<Pixels>) -> Self;
}

impl ShiftBounds for Bounds<Pixels> {
  fn shift(self, by: Point<Pixels>) -> Self {
    Bounds::new(self.origin + by, self.size)
  }
}

// -------- Edit / movement helper free functions ------------------------

fn document_text_slice(document: &Document, range: Range<usize>) -> String {
  let mut text = String::with_capacity(range.end - range.start);
  for chunk in document.text.byte_slice(range).chunks() {
    text.push_str(chunk);
  }
  text
}

fn shift_paragraphs_after(document: &mut Document, paragraph_ix: usize, delta: isize) {
  if delta == 0 {
    return;
  }
  for paragraph in document.paragraphs.iter_mut().skip(paragraph_ix + 1) {
    paragraph.byte_range = shift_range(paragraph.byte_range.clone(), delta);
  }
}

fn shift_range(range: Range<usize>, delta: isize) -> Range<usize> {
  if delta >= 0 {
    let delta = delta as usize;
    range.start + delta..range.end + delta
  } else {
    let delta = (-delta) as usize;
    range.start - delta..range.end - delta
  }
}

fn split_runs_at(runs: &[TextRun], byte: usize) -> (Vec<TextRun>, Vec<TextRun>) {
  let mut left = Vec::new();
  let mut right = Vec::new();
  let mut offset = 0;
  for run in runs {
    let run_start = offset;
    let run_end = offset + run.len;
    offset = run_end;
    if run_end <= byte {
      left.push(run.clone());
    } else if run_start >= byte {
      right.push(run.clone());
    } else {
      let left_len = byte - run_start;
      let right_len = run_end - byte;
      if left_len > 0 {
        left.push(TextRun {
          len: left_len,
          styles: run.styles,
        });
      }
      if right_len > 0 {
        right.push(TextRun {
          len: right_len,
          styles: run.styles,
        });
      }
    }
  }
  (merge_adjacent_runs(left), merge_adjacent_runs(right))
}

fn split_paragraph_at(document: &mut Document, paragraph_ix: usize, byte: usize) {
  let paragraph = document.paragraphs[paragraph_ix].clone();
  let global = paragraph.byte_range.start + byte;
  document.text.insert(global, "\n");
  let (left_runs, right_runs) = split_runs_at(&paragraph.runs, byte);
  let old_end = paragraph.byte_range.end;
  document.paragraphs[paragraph_ix].byte_range = paragraph.byte_range.start..global;
  document.paragraphs[paragraph_ix].runs = left_runs;
  document.paragraphs.insert(
    paragraph_ix + 1,
    Paragraph {
      style: paragraph.style,
      byte_range: global + 1..old_end + 1,
      runs: right_runs,
    },
  );
  for paragraph in document.paragraphs.iter_mut().skip(paragraph_ix + 2) {
    paragraph.byte_range = shift_range(paragraph.byte_range.clone(), 1);
  }
}

fn delete_cross_paragraph_range(document: &mut Document, range: Range<DocumentOffset>) {
  if range.start.paragraph >= range.end.paragraph {
    delete_range_in_paragraph(document, range.start.paragraph, range.start.byte..range.end.byte);
    return;
  }

  let start_ix = range.start.paragraph;
  let end_ix = range.end.paragraph;
  let start_para = document.paragraphs[start_ix].clone();
  let end_para = document.paragraphs[end_ix].clone();
  let start_global = start_para.byte_range.start + range.start.byte;
  let end_global = end_para.byte_range.start + range.end.byte;
  let delete_len = end_global - start_global;

  let (left_runs, _) = split_runs_at(&start_para.runs, range.start.byte);
  let (_, right_runs) = split_runs_at(&end_para.runs, range.end.byte);
  document.text.delete(start_global..end_global);

  let mut merged_runs = left_runs;
  merged_runs.extend(right_runs);
  document.paragraphs[start_ix].runs = merge_adjacent_runs(merged_runs);
  document.paragraphs[start_ix].byte_range =
    start_para.byte_range.start..start_para.byte_range.start + paragraph_runs_len(&document.paragraphs[start_ix]);
  document.paragraphs.drain(start_ix + 1..=end_ix);
  for paragraph in document.paragraphs.iter_mut().skip(start_ix + 1) {
    paragraph.byte_range = shift_range(paragraph.byte_range.clone(), -(delete_len as isize));
  }
}

fn paragraph_runs_len(paragraph: &Paragraph) -> usize {
  paragraph.runs.iter().map(|run| run.len).sum()
}

// Returns `(run_index, local_byte)` for the given absolute byte offset within
// the paragraph. Biases to the LEFT run at run boundaries — i.e. when `byte`
// equals the end of run i and the start of run i+1, we return run i. This is
// what lets typed text inherit styles from the run "just before the caret".
fn run_containing(paragraph: &Paragraph, byte: usize) -> (usize, usize) {
  let mut offset = 0;
  for (ix, run) in paragraph.runs.iter().enumerate() {
    let run_end = offset + run.len;
    if byte <= run_end {
      return (ix, byte - offset);
    }
    offset = run_end;
  }
  // byte is beyond the end — clamp to the last run.
  if paragraph.runs.is_empty() {
    (0, 0)
  } else {
    let last = paragraph.runs.len() - 1;
    (last, paragraph.runs[last].len)
  }
}

// Inserts `text` (with `styles`) into `paragraph` at `byte`. Splits the run
// straddling the byte if needed and re-merges adjacent runs with identical
// styles afterwards.
fn insert_text_at(document: &mut Document, paragraph_ix: usize, byte: usize, text: &str, styles: RunStyles) {
  if text.is_empty() {
    return;
  }
  let insert_len = text.len();
  let paragraph_start = document.paragraphs[paragraph_ix].byte_range.start;
  document.text.insert(paragraph_start + byte, text);
  shift_paragraphs_after(document, paragraph_ix, insert_len as isize);
  let paragraph = &mut document.paragraphs[paragraph_ix];
  paragraph.byte_range.end += insert_len;
  if paragraph.runs.is_empty() {
    paragraph.runs.push(TextRun { len: insert_len, styles });
    return;
  }

  let mut offset = 0;
  for i in 0..paragraph.runs.len() {
    let run_start = offset;
    let run_len = paragraph.runs[i].len;
    let run_end = run_start + run_len;
    if byte <= run_end {
      let local = byte - run_start;

      if paragraph.runs[i].styles == styles {
        paragraph.runs[i].len += insert_len;
        return;
      }

      if local == 0 {
        if i > 0 && paragraph.runs[i - 1].styles == styles {
          paragraph.runs[i - 1].len += insert_len;
        } else {
          paragraph
            .runs
            .insert(i, TextRun { len: insert_len, styles });
        }
        return;
      }

      if local == run_len {
        if i + 1 < paragraph.runs.len() && paragraph.runs[i + 1].styles == styles {
          paragraph.runs[i + 1].len += insert_len;
        } else {
          paragraph
            .runs
            .insert(i + 1, TextRun { len: insert_len, styles });
        }
        return;
      }

      let run_styles = paragraph.runs[i].styles;
      let right_len = run_len - local;
      paragraph.runs[i].len = local;
      paragraph
        .runs
        .insert(i + 1, TextRun { len: insert_len, styles });
      paragraph.runs.insert(
        i + 2,
        TextRun {
          len: right_len,
          styles: run_styles,
        },
      );
      return;
    }
    offset = run_end;
  }

  if let Some(last) = paragraph.runs.last_mut() {
    if last.styles == styles {
      last.len += insert_len;
    } else {
      paragraph.runs.push(TextRun { len: insert_len, styles });
    }
  }
}

// Removes the half-open byte range `[range.start, range.end)` from
// `paragraph`. Runs are split or dropped as needed; remaining runs are re-
// merged so adjacent same-style fragments coalesce.
fn delete_range_in_paragraph(document: &mut Document, paragraph_ix: usize, range: Range<usize>) {
  if range.start >= range.end {
    return;
  }
  let delete_len = range.end - range.start;
  let paragraph_start = document.paragraphs[paragraph_ix].byte_range.start;
  document
    .text
    .delete(paragraph_start + range.start..paragraph_start + range.end);
  shift_paragraphs_after(document, paragraph_ix, -(delete_len as isize));
  let paragraph = &mut document.paragraphs[paragraph_ix];
  paragraph.byte_range.end -= delete_len;
  let mut offset = 0;
  let mut new_runs: Vec<TextRun> = Vec::with_capacity(paragraph.runs.len());
  for run in paragraph.runs.drain(..) {
    let run_start = offset;
    let run_end = offset + run.len;
    offset = run_end;
    if run_end <= range.start || run_start >= range.end {
      new_runs.push(run);
      continue;
    }
    let local_start = range.start.saturating_sub(run_start).min(run.len);
    let local_end = range.end.saturating_sub(run_start).min(run.len);
    let removed = local_end - local_start;
    let remaining = run.len - removed;
    if remaining > 0 {
      new_runs.push(TextRun {
        len: remaining,
        styles: run.styles,
      });
    }
  }
  paragraph.runs = merge_adjacent_runs(new_runs);
}

// Locate the `LaidOutLine` containing the given offset. Returns
// `(paragraph_layout_index, line_index)`. When the byte sits exactly on a
// soft-wrap seam (== end_byte of line k and start_byte of line k+1), we bias
// to the next line — matching Word's "caret-at-start-of-next-line"
// convention. This is exactly the disambiguation called out in the plan.
fn locate_line(layout: &LayoutState, off: DocumentOffset) -> Option<(usize, usize)> {
  let p_ix = paragraph_layout_index(layout, off.paragraph)?;
  let para = &layout.paragraphs[p_ix];
  for (i, line) in para.lines.iter().enumerate() {
    if off.byte >= line.start_byte && off.byte <= line.end_byte {
      // Bias to next line at exact wrap seam.
      if off.byte == line.end_byte && i + 1 < para.lines.len() && para.lines[i + 1].start_byte == off.byte {
        return Some((p_ix, i + 1));
      }
      return Some((p_ix, i));
    }
  }
  // Fall back to last line of the paragraph (e.g. byte == para.len after a
  // soft-wrapped trailing whitespace strip).
  let last = para.lines.len().checked_sub(1)?;
  Some((p_ix, last))
}

fn paragraph_layout(layout: &LayoutState, paragraph: usize) -> Option<&LaidOutParagraph> {
  layout
    .paragraphs
    .get(paragraph)
    .filter(|layout_paragraph| layout_paragraph.index == paragraph)
    .or_else(|| {
      layout
        .paragraphs
        .iter()
        .find(|layout_paragraph| layout_paragraph.index == paragraph)
    })
}

fn paragraph_layout_index(layout: &LayoutState, paragraph: usize) -> Option<usize> {
  if layout
    .paragraphs
    .get(paragraph)
    .is_some_and(|layout_paragraph| layout_paragraph.index == paragraph)
  {
    Some(paragraph)
  } else {
    layout
      .paragraphs
      .iter()
      .position(|layout_paragraph| layout_paragraph.index == paragraph)
  }
}

// Step to the previous visual line. If we're already on the first line of a
// paragraph, jump to the last line of the previous paragraph.
fn find_line_above(layout: &LayoutState, p_ix: usize, line_ix: usize) -> Option<(usize, usize)> {
  if line_ix > 0 {
    return Some((p_ix, line_ix - 1));
  }
  if p_ix == 0 {
    return None;
  }
  let prev = p_ix - 1;
  let last = layout.paragraphs[prev].lines.len().checked_sub(1)?;
  Some((prev, last))
}

fn find_line_below(layout: &LayoutState, p_ix: usize, line_ix: usize) -> Option<(usize, usize)> {
  if line_ix + 1 < layout.paragraphs[p_ix].lines.len() {
    return Some((p_ix, line_ix + 1));
  }
  if p_ix + 1 < layout.paragraphs.len() {
    return Some((p_ix + 1, 0));
  }
  None
}

// Grapheme-cluster-aware step backwards. Handles combining marks and
// compound emoji correctly, so one keystroke deletes one visible character.
fn prev_grapheme_boundary_in_paragraph(document: &Document, paragraph_ix: usize, byte: usize) -> usize {
  if byte == 0 {
    return 0;
  }
  // Fast path for the common ASCII case. ASCII bytes are always single-byte
  // grapheme clusters, so we can avoid allocating the paragraph text while
  // someone is holding an arrow/delete key down through ordinary prose.
  if paragraph_byte_at(document, paragraph_ix, byte - 1).is_some_and(|byte| byte.is_ascii()) {
    return byte - 1;
  }
  let text = paragraph_text(document, &document.paragraphs[paragraph_ix]);
  prev_grapheme_boundary(&text, byte)
}

fn next_grapheme_boundary_in_paragraph(document: &Document, paragraph_ix: usize, byte: usize) -> usize {
  let paragraph = &document.paragraphs[paragraph_ix];
  let len = paragraph_text_len(paragraph);
  if byte >= len {
    return len;
  }
  if paragraph_byte_at(document, paragraph_ix, byte).is_some_and(|byte| byte.is_ascii()) {
    return byte + 1;
  }
  let text = paragraph_text(document, paragraph);
  next_grapheme_boundary(&text, byte)
}

fn paragraph_byte_at(document: &Document, paragraph_ix: usize, byte: usize) -> Option<u8> {
  let paragraph = document.paragraphs.get(paragraph_ix)?;
  (byte < paragraph_text_len(paragraph)).then(|| document.text.byte(paragraph.byte_range.start + byte))
}

fn prev_grapheme_boundary(s: &str, byte: usize) -> usize {
  let mut cursor = GraphemeCursor::new(byte, s.len(), true);
  cursor.prev_boundary(s, 0).ok().flatten().unwrap_or(0)
}

fn next_grapheme_boundary(s: &str, byte: usize) -> usize {
  let mut cursor = GraphemeCursor::new(byte, s.len(), true);
  cursor.next_boundary(s, 0).ok().flatten().unwrap_or(s.len())
}

fn validate_document(document: &Document) -> io::Result<()> {
  let text_len = document.text.byte_len();
  if document.paragraphs.is_empty() {
    return Err(io::Error::new(io::ErrorKind::InvalidData, "DB8 document has no paragraphs"));
  }
  for (ix, paragraph) in document.paragraphs.iter().enumerate() {
    if paragraph.byte_range.start > paragraph.byte_range.end || paragraph.byte_range.end > text_len {
      return Err(io::Error::new(io::ErrorKind::InvalidData, "paragraph range is outside document text"));
    }
    if ix == 0 && paragraph.byte_range.start != 0 {
      return Err(io::Error::new(io::ErrorKind::InvalidData, "first paragraph does not start at byte 0"));
    }
    if paragraph_runs_len(paragraph) != paragraph_text_len(paragraph) {
      return Err(io::Error::new(
        io::ErrorKind::InvalidData,
        "paragraph run lengths do not match paragraph text",
      ));
    }
    if ix > 0 {
      let previous = &document.paragraphs[ix - 1];
      if previous.byte_range.end + 1 != paragraph.byte_range.start || document.text.byte(previous.byte_range.end) != b'\n' {
        return Err(io::Error::new(io::ErrorKind::InvalidData, "paragraph ranges are not newline separated"));
      }
    }
  }
  if document
    .paragraphs
    .last()
    .is_some_and(|paragraph| paragraph.byte_range.end != text_len)
  {
    return Err(io::Error::new(io::ErrorKind::InvalidData, "last paragraph does not end at text length"));
  }
  Ok(())
}

fn read_u8(cursor: &mut Cursor<&[u8]>) -> io::Result<u8> {
  let mut bytes = [0; 1];
  cursor.read_exact(&mut bytes)?;
  Ok(bytes[0])
}

fn read_u32(cursor: &mut Cursor<&[u8]>) -> io::Result<u32> {
  let mut bytes = [0; 4];
  cursor.read_exact(&mut bytes)?;
  Ok(u32::from_le_bytes(bytes))
}

fn read_u64(cursor: &mut Cursor<&[u8]>) -> io::Result<u64> {
  let mut bytes = [0; 8];
  cursor.read_exact(&mut bytes)?;
  Ok(u64::from_le_bytes(bytes))
}

fn write_u64(bytes: &mut Vec<u8>, value: u64) {
  bytes.extend_from_slice(&value.to_le_bytes());
}

fn encode_paragraph_style(style: ParagraphStyle) -> u8 {
  match style {
    ParagraphStyle::Pocket => 0,
    ParagraphStyle::Hat => 1,
    ParagraphStyle::Block => 2,
    ParagraphStyle::Tag => 3,
    ParagraphStyle::Analytic => 4,
    ParagraphStyle::Normal => 5,
    ParagraphStyle::Undertag => 6,
  }
}

fn decode_paragraph_style(value: u8) -> io::Result<ParagraphStyle> {
  match value {
    0 => Ok(ParagraphStyle::Pocket),
    1 => Ok(ParagraphStyle::Hat),
    2 => Ok(ParagraphStyle::Block),
    3 => Ok(ParagraphStyle::Tag),
    4 => Ok(ParagraphStyle::Analytic),
    5 => Ok(ParagraphStyle::Normal),
    6 => Ok(ParagraphStyle::Undertag),
    _ => Err(io::Error::new(io::ErrorKind::InvalidData, "invalid paragraph style")),
  }
}

fn encode_run_styles(styles: RunStyles) -> u8 {
  let mut bits = 0;
  if styles.cite {
    bits |= 1 << 0;
  }
  if styles.direct_underline {
    bits |= 1 << 1;
  }
  if styles.emphasis {
    bits |= 1 << 2;
  }
  if styles.style_underline {
    bits |= 1 << 3;
  }
  bits
    | match styles.highlight {
      None => 0,
      Some(HighlightStyle::Spoken) => 1 << 4,
      Some(HighlightStyle::Insert) => 2 << 4,
      Some(HighlightStyle::Alternative) => 3 << 4,
    }
}

fn decode_run_styles(bits: u8) -> io::Result<RunStyles> {
  let highlight = match (bits >> 4) & 0b11 {
    0 => None,
    1 => Some(HighlightStyle::Spoken),
    2 => Some(HighlightStyle::Insert),
    3 => Some(HighlightStyle::Alternative),
    _ => unreachable!(),
  };
  if bits & 0b1100_0000 != 0 {
    return Err(io::Error::new(io::ErrorKind::InvalidData, "invalid run style bits"));
  }
  Ok(RunStyles {
    cite: bits & (1 << 0) != 0,
    direct_underline: bits & (1 << 1) != 0,
    emphasis: bits & (1 << 2) != 0,
    style_underline: bits & (1 << 3) != 0,
    highlight,
  })
}

fn document_from_input(theme: DocumentTheme, paragraphs: Vec<InputParagraph>) -> Document {
  let mut text = String::new();
  let mut stored_paragraphs = Vec::with_capacity(paragraphs.len());
  for (ix, paragraph) in paragraphs.into_iter().enumerate() {
    if ix > 0 {
      text.push('\n');
    }
    let start = text.len();
    let mut runs = Vec::with_capacity(paragraph.runs.len());
    for run in paragraph.runs {
      let len = run.text.len();
      text.push_str(&run.text);
      runs.push(TextRun { len, styles: run.styles });
    }
    let end = text.len();
    stored_paragraphs.push(Paragraph {
      style: paragraph.style,
      byte_range: start..end,
      runs: merge_adjacent_runs(runs),
    });
  }
  if stored_paragraphs.is_empty() {
    stored_paragraphs.push(Paragraph {
      style: ParagraphStyle::Normal,
      byte_range: 0..0,
      runs: Vec::new(),
    });
  }
  Document {
    text: Rope::from(text),
    paragraphs: stored_paragraphs,
    theme,
  }
}

