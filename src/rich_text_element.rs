#[derive(Clone, Default)]
struct WordElementLayout(Rc<RefCell<Option<Rc<LayoutState>>>>);

#[derive(Clone)]
struct LayoutState {
  paragraphs: Vec<LaidOutParagraph>,
  bounds: Option<Bounds<Pixels>>,
  size: Size<Pixels>,
  width: Pixels,
  snap_underline_rules_to_pixels: bool,
}

impl LayoutState {
  fn hit_test(&self, position: Point<Pixels>) -> DocumentOffset {
    let position = match self.bounds {
      Some(bounds) => position - bounds.origin,
      None => position,
    };
    for paragraph in &self.paragraphs {
      if position.y < paragraph.top {
        return DocumentOffset {
          paragraph: paragraph.index,
          byte: 0,
        };
      }
      if position.y <= paragraph.bottom {
        return paragraph.hit_test(position);
      }
    }
    let Some(last) = self.paragraphs.last() else {
      return DocumentOffset::default();
    };
    DocumentOffset {
      paragraph: last.index,
      byte: last.len,
    }
  }
}

#[derive(Clone)]
struct LaidOutParagraph {
  index: usize,
  cache_key: ParagraphCacheKey,
  len: usize,
  top: Pixels,
  bottom: Pixels,
  lines: Vec<LaidOutLine>,
  borders: Vec<RunRect>,
}

#[derive(Clone, Copy, Debug, Eq, PartialEq)]
struct ParagraphCacheKey {
  fingerprint: u64,
}

fn paragraph_cache_key(document: &Document, paragraph: &Paragraph) -> ParagraphCacheKey {
  let mut hasher = DefaultHasher::new();
  paragraph.style.hash(&mut hasher);
  paragraph_text(document, paragraph).hash(&mut hasher);
  for run in &paragraph.runs {
    run.len.hash(&mut hasher);
    run.styles.hash(&mut hasher);
  }
  ParagraphCacheKey {
    fingerprint: hasher.finish(),
  }
}

impl LaidOutParagraph {
  fn shift_y(&mut self, new_top: Pixels) {
    let delta = new_top - self.top;
    self.top += delta;
    self.bottom += delta;
    for line in &mut self.lines {
      line.origin.y += delta;
    }
    for border in &mut self.borders {
      border.bounds.origin.y += delta;
    }
  }

  fn hit_test(&self, position: Point<Pixels>) -> DocumentOffset {
    for line in &self.lines {
      if position.y <= line.origin.y + line.line_height {
        return DocumentOffset {
          paragraph: self.index,
          byte: line.hit_test_x(position.x - line.origin.x),
        };
      }
    }
    DocumentOffset {
      paragraph: self.index,
      byte: self.len,
    }
  }
}

#[derive(Clone)]
struct LaidOutLine {
  origin: Point<Pixels>,
  line_height: Pixels,
  ascent: Pixels,
  descent: Pixels,
  width: Pixels,
  start_byte: usize,
  end_byte: usize,
  segments: Vec<LaidOutSegment>,
  rects: Vec<RunRect>,
  underlines: Vec<Decoration>,
}

impl LaidOutLine {
  fn baseline_y(&self) -> Pixels {
    ((self.line_height - self.ascent - self.descent) / 2.0) + self.ascent
  }

  fn hit_test_x(&self, x: Pixels) -> usize {
    for segment in &self.segments {
      if x <= segment.x + segment.width {
        let local_x = (x - segment.x).max(px(0.0));
        return segment.start_byte + segment.shaped.closest_index_for_x(local_x);
      }
    }
    self.end_byte
  }
}

